const LAND_MAP: number[][] = RAW_MAP.map((row) =>
  row.split("").map(Number)
);

// ── Map lookups ────────────────────────────────────────────────

function isLand(lon: number, lat: number): boolean {
  let x = Math.floor(((lon + Math.PI) / (2 * Math.PI)) * MAP_W);
  let y = Math.floor(((Math.PI / 2 - lat) / Math.PI) * MAP_H);
  x = ((x % MAP_W) + MAP_W) % MAP_W;
  y = Math.max(0, Math.min(MAP_H - 1, y));
  return LAND_MAP[y][x] === 1;
}

function isCoast(lon: number, lat: number): boolean {
  const step = 0.12;
  const here = isLand(lon, lat);
  for (let dlat = -step; dlat <= step; dlat += step) {
    for (let dlon = -step; dlon <= step; dlon += step) {
      if (dlat === 0 && dlon === 0) continue;
      if (isLand(lon + dlon, lat + dlat) !== here) return true;
    }
  }
  return false;
}

function isDense(lon: number, lat: number): boolean {
  return (
    isLand(lon + 0.2, lat) &&
    isLand(lon - 0.2, lat) &&
    isLand(lon, lat + 0.15) &&
    isLand(lon, lat - 0.15)
  );
}

// ── Precomputed High-Res Map ───────────────────────────────────

const MAP_RES_X = 1440;
const MAP_RES_Y = 720;
const PRECOMPUTED_MAP = new Uint8Array(MAP_RES_X * MAP_RES_Y);

let _mapInitialized = false;
function initGlobeMap() {
  if (_mapInitialized) return;
  for (let y = 0; y < MAP_RES_Y; y++) {
    const lat = Math.PI / 2 - (y / (MAP_RES_Y - 1)) * Math.PI; 
    for (let x = 0; x < MAP_RES_X; x++) {
      const lon = (x / MAP_RES_X) * 2 * Math.PI - Math.PI;
      
      let type = 0;
      if (isLand(lon, lat)) {
        if (isCoast(lon, lat)) type = 2; // coast
        else if (isDense(lon, lat)) type = 3; // dense
        else type = 1; // land
      }
      PRECOMPUTED_MAP[y * MAP_RES_X + x] = type;
    }
  }
  _mapInitialized = true;
}

// ── Default config ─────────────────────────────────────────────

const DEFAULT_CONFIG: GlobeConfig = {
  fontPx: 9,
  lineH: 1.25,
  rotSpeed: 0.0028,
  tilt: 0.38,
  globeRadius: 0.37,
  colors: {
    bg:    "#eeebe4",
    grid:  "#c2bfb8",
    ocean: "#9b9891",
    coast: "#4a4845",
    land:  "#1a1a1a",
    dense: "#0a0a0a",
  },
  chars: {
    grid:  "\u00B7", // ·
    ocean: ".",
    coast: "+",
    land:  "#",
    dense: "@",
  },
};

// ── Renderer class ─────────────────────────────────────────────

export class GlobeRenderer {
  private canvas: HTMLCanvasElement;
  private ctx: CanvasRenderingContext2D;
  private cfg: GlobeConfig;
  private state: GlobeState;
  private onLonUpdate?: (deg: number) => void;
  
  private gridCanvas: HTMLCanvasElement | null = null;
  private globePoints: GlobePoint[] = [];

  constructor(
    canvas: HTMLCanvasElement,
    config: Partial<GlobeConfig> = {},
    onLonUpdate?: (deg: number) => void
  ) {
    // alpha: false explicitly speeds up drawing when the canvas is fully opaque
    const ctx = canvas.getContext("2d", { alpha: false });
    if (!ctx) throw new Error("Could not get 2D context from canvas");

    initGlobeMap();

    this.canvas = canvas;
    this.ctx = ctx;
    this.cfg = { ...DEFAULT_CONFIG, ...config };
    this.onLonUpdate = onLonUpdate;
    this.state = {
      w: 0, h: 0,
      cols: 0, rows: 0,
      cw: 0, ch: 0,
      cx: 0, cy: 0,
      r: 0,
      rotation: 0,
      rafId: 0,
      last: 0,
    };

    this.resize();
    window.addEventListener("resize", this.resize);
    this.state.rafId = requestAnimationFrame(this.loop);
  }

  // ── Public ───────────────────────────────────────────────────

  /** Stop the animation and remove resize listener */
  destroy(): void {
    this.pause();
    window.removeEventListener("resize", this.resize);
  }

  /** Pause rendering */
  pause(): void {
    if (this.state.rafId) {
      cancelAnimationFrame(this.state.rafId);
      this.state.rafId = 0;
    }
  }

  /** Resume rendering */
  resume(): void {
    if (!this.state.rafId) {
      this.state.last = 0; // reset dt calculation to prevent huge jump
      this.state.rafId = requestAnimationFrame(this.loop);
    }
  }

  /** Update config at runtime (e.g. toggle dark/light) */
  setConfig(patch: Partial<GlobeConfig>): void {
    this.cfg = { ...this.cfg, ...patch };
    this.resize(); // trigger a re-render of static grid if config changes
  }

  // ── Private ──────────────────────────────────────────────────

  private resize = (): void => {
    const { cfg, ctx, canvas, state } = this;
    const dpr = window.devicePixelRatio || 1;
    const parent = canvas.parentElement;
    const w   = parent ? parent.clientWidth : window.innerWidth;
    const h   = parent ? parent.clientHeight : window.innerHeight;

    canvas.width  = Math.round(w * dpr);
    canvas.height = Math.round(h * dpr);
    canvas.style.width  = `${w}px`;
    canvas.style.height = `${h}px`;

    ctx.setTransform(1, 0, 0, 1, 0, 0);
    ctx.scale(dpr, dpr);
    ctx.font = this.font();

    const cw = ctx.measureText("M").width;
    const ch = cfg.fontPx * cfg.lineH;

    const isMobile = w < 640;

    state.w    = w;
    state.h    = h;
    state.cw   = cw;
    state.ch   = ch;
    state.cols = Math.ceil(w / cw) + 2;
    state.rows = Math.ceil(h / ch) + 2;
    state.cx   = w / 2;
    state.cy   = isMobile ? h * 0.5 : h / 2;
    state.r    = Math.min(w, h) * (isMobile ? 0.40 : cfg.globeRadius);

    this.preRenderGrid(w, h, dpr);
    this.precomputeGlobePoints();
  };
  
  private preRenderGrid(w: number, h: number, dpr: number): void {
    if (!this.gridCanvas) {
      this.gridCanvas = document.createElement("canvas");
    }
    this.gridCanvas.width = this.canvas.width;
    this.gridCanvas.height = this.canvas.height;
    
    const gCtx = this.gridCanvas.getContext("2d", { alpha: false });
    if (!gCtx) return;
    
    gCtx.setTransform(1, 0, 0, 1, 0, 0);
    gCtx.scale(dpr, dpr);
    
    gCtx.fillStyle = this.cfg.colors.bg;
    gCtx.fillRect(0, 0, w, h);
    
    // Only draw grid dots on desktop (skip on mobile for a cleaner look)
    if (w >= 640) {
      gCtx.font = this.font();
      gCtx.fillStyle = this.cfg.colors.grid;

      const { rows, cols, ch, cw, cx, cy, r } = this.state;
      for (let row = 0; row < rows; row++) {
        const py = row * ch + ch;
        for (let col = 0; col < cols; col++) {
          const px = col * cw;
          const dx = (px - cx) / r;
          const dy = (py - cy) / r;
          // Outside the globe radius? Render grid.
          if (dx * dx + dy * dy > 1.0) {
            gCtx.fillText(this.cfg.chars.grid, px, py);
          }
        }
      }
    }
  }
  
  private precomputeGlobePoints(): void {
    this.globePoints = [];
    const { rows, cols, ch, cw, cx, cy, r } = this.state;
    const cosT = Math.cos(this.cfg.tilt);
    const sinT = Math.sin(this.cfg.tilt);

    for (let row = 0; row < rows; row++) {
      const py = row * ch + ch;
      for (let col = 0; col < cols; col++) {
        const px = col * cw;
        const dx = (px - cx) / r;
        const dy = (py - cy) / r;
        const d2 = dx * dx + dy * dy;

        // Inside the globe radius? Store as globe character to render dynamically.
        if (d2 <= 1.0) {
          const dz  = Math.sqrt(1 - d2);
          const dy2 = dy * cosT - dz * sinT;
          const dz2 = dy * sinT + dz * cosT;
          
          const lat = Math.asin(Math.max(-1, Math.min(1, -dy2)));
          const lonOffset = Math.atan2(dx, dz2);
          
          this.globePoints.push({ x: px, y: py, lat, lonOffset });
        }
      }
    }
  }

  private font(): string {
    return `${this.cfg.fontPx}px "Courier New", Courier, monospace`;
  }

  private draw(): void {
    const { cfg, ctx, state } = this;
    const { w, h, rotation } = state;

    // Fast static background paint
    if (this.gridCanvas) {
      // By passing w/h we map internal width properly avoiding subpixel blurring
      ctx.drawImage(this.gridCanvas, 0, 0, w, h);
    } else {
      ctx.fillStyle = cfg.colors.bg;
      ctx.fillRect(0, 0, w, h);
    }

    ctx.font = this.font();

    let lastColor = "";
    
    // Cache map props for fast loop execution
    const cocean = cfg.colors.ocean;
    const ccoast = cfg.colors.coast;
    const cland = cfg.colors.land;
    const cdense = cfg.colors.dense;
    
    const chocean = cfg.chars.ocean;
    const chcoast = cfg.chars.coast;
    const chland = cfg.chars.land;
    const chdense = cfg.chars.dense;

    // Evaluate purely the globe points
    for (let i = 0; i < this.globePoints.length; i++) {
        const pt = this.globePoints[i];
        const lon = pt.lonOffset + rotation;
        const lat = pt.lat;
        
        let color: string;
        let char: string;
        
        // Lookup in high-res precomputed map
        const normLon = ((lon + Math.PI) / (2 * Math.PI)) % 1;
        const xAmt = normLon >= 0 ? normLon : normLon + 1; // 0 to 1
        const yAmt = (Math.PI / 2 - lat) / Math.PI;        // 0 to 1
        
        const pxMap = Math.floor(xAmt * MAP_RES_X) % MAP_RES_X;
        let pyMap = Math.floor(yAmt * (MAP_RES_Y - 1));
        if (pyMap < 0) pyMap = 0;
        if (pyMap >= MAP_RES_Y) pyMap = MAP_RES_Y - 1;

        const type = PRECOMPUTED_MAP[pyMap * MAP_RES_X + pxMap];
        
        if (type === 1) {
            color = cland;
            char = chland;
        } else if (type === 2) {
            color = ccoast;
            char = chcoast;
        } else if (type === 3) {
            color = cdense;
            char = chdense;
        } else {
            color = cocean;
            char = chocean;
        }
        
        if (color !== lastColor) {
            ctx.fillStyle = color;
            lastColor = color;
        }
        ctx.fillText(char, pt.x, pt.y);
    }
  }

  private loop = (ts: number): void => {
    const { state, cfg } = this;
    const dt = state.last ? Math.min((ts - state.last) / 16.67, 3) : 1;
    state.last = ts;
    state.rotation += cfg.rotSpeed * dt;

    this.draw();

    if (this.onLonUpdate) {
      const deg = ((state.rotation * 180 / Math.PI) % 360 + 360) % 360;
      this.onLonUpdate(deg);
    }

    state.rafId = requestAnimationFrame(this.loop);
  };
}

// ── Types ──────────────────────────────────────────────────────

interface GlobeConfig {
  fontPx: number;
  lineH: number;
  rotSpeed: number;
  tilt: number;
  globeRadius: number;
  colors: {
    bg: string;
    grid: string;
    ocean: string;
    coast: string;
    land: string;
    dense: string;
  };
  chars: {
    grid: string;
    ocean: string;
    coast: string;
    land: string;
    dense: string;
  };
}

interface GlobePoint {
  x: number;
  y: number;
  lat: number;
  lonOffset: number;
}

interface GlobeState {
  w: number;
  h: number;
  cols: number;
  rows: number;
  cw: number;
  ch: number;
  cx: number;
  cy: number;
  r: number;
  rotation: number;
  rafId: number;
  last: number;
}

